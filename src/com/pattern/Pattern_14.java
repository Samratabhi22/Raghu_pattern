package com.pattern;

public class Pattern_14 {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=i;j++) {
				for(int k=j;k<=j;k=k+4) {
				if(i%2!=0) {
					System.out.print(k);
				}
				else {
					System.out.print(k);
				}
				
			}
			}
			System.out.println();
	}
		
	}
}
